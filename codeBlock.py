class CodeBlock:
 def generate(self):
  plano = []
  cont = 0
  while cont < 1:
   plano.append('GA')
   cont += 1
  cont = 0
  while cont < 4:
   plano.append('M')
   cont += 1
  cont = 0
  while cont < 1:
   plano.append('GA')
   cont += 1
  cont = 0
  while cont < 3:
   plano.append('M')
   cont += 1
  return plano